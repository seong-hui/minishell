#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*str;
	int		str_len;
	int		i;

	str = (char *)s;
	str_len = ft_strlen(str);
	i = 0;
	while (i <= str_len)
	{
		if (str[i] == (unsigned char)c)
		{
			return (&str[i]);
		}
		i++;
	}
	return (0);
}
